import turtle
t=turtle.Turtle('turtle')
t.shapesize(3)
t.speed(0)
t.pencolor('black')
t.pensize(15)
t.circle(100,360)
t.penup()
t.backward(220)
t.pencolor('blue')
t.pendown()
t.circle(100,360)
t.penup()
t.goto(220,0)
t.pencolor('red')
t.pendown()
t.circle(100,360)
t.penup()
t.goto(-110,-110)
t.pendown()
t.pencolor('yellow')
t.circle(100,360)
t.penup()
t.goto(110,-110)
t.pendown()
t.pencolor('green')
t.circle(100,360)
turtle.done()