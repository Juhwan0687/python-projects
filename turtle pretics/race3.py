import turtle
from random import*
t=turtle.Turtle()
t1=turtle.Turtle()
t2=turtle.Turtle()
t.speed(0)
turtle.addshape('bunny.gif')
turtle.addshape('turtle.gif')
t1.shape('turtle.gif')
t2.shape('bunny.gif')
t1.pencolor('#13d14f')
t2.pencolor('#f2c2e7')
t.penup()
t.goto(-400,320)
t.write("1")
t.goto(-400,300)
t.pendown()
t.goto(-400,-300)
t.penup()
t.goto(-90,320)
t.write("2")
t.goto(-90,300)
t.pendown()
t.goto(-90,-300)
t.penup()
t.goto(200,320)
t.write("3")
t.goto(200,300)
t.pendown()
t.goto(200,-300)
t1.penup()
t2.penup()
t1.goto(400,150)
t2.goto(320,-200)
t1.pendown()
t2.pendown()
a=randint(-450,400)
b=randint(-450,320)
t1.goto(a,150)
t2.goto(b,-200)
if(a<b):
    t.penup()
    t.goto(600,200)
    t.write("거북이 승리!")
if(b<a):
    t.penup()
    t.goto(600,200)
    t.write("토끼 승리!")
turtle.done()